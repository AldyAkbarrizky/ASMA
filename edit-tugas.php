    $table2 = $database->MateriKuliah;
    $kelas = $table->findOne(['_id' => new MongoDB\BSON\ObjectID($id_kelas)]);
    $matkul = $table2->find(array('id_kelas' => $kelas['id_kelas']));
    $tugas_list = $table->aggregate(array(
            array(
                '$match' => array(
                    '_id' => new MongoDB\BSON\ObjectID($id_kelas)
                ),
            ),
            array(
                '$replaceRoot' => array(
                    'newRoot' => array(
                        '$first' => array(
                            '$filter' => array(
                                'input' => '$tugas',
                                'as' => 'tugas_kuliah',
                                'cond' => array(
                                    '$eq' => [
                                        '$$tugas_kuliah.id_tugas', new MongoDB\BSON\ObjectID($id_tugas)
                                    ]
                                )
                            )
                        )
                    )
